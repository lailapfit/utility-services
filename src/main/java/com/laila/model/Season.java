package com.laila.model;

public class Season {
	
	private String fallBeginning;
	private String fallEnd;
	private String winterBeginning;
	private String winterEnd;
	private String summerBeginning;
	private String summerEnd;
	private String springBeginning;
	private String springEnd;
	
	public String getFallBeginning() {
		return fallBeginning;
	}
	public void setFallBeginning(String fallBeginning) {
		this.fallBeginning = fallBeginning;
	}
	public String getFallEnd() {
		return fallEnd;
	}
	public void setFallEnd(String fallEnd) {
		this.fallEnd = fallEnd;
	}
	public String getWinterBeginning() {
		return winterBeginning;
	}
	public void setWinterBeginning(String winterBeginning) {
		this.winterBeginning = winterBeginning;
	}
	public String getWinterEnd() {
		return winterEnd;
	}
	public void setWinterEnd(String winterEnd) {
		this.winterEnd = winterEnd;
	}
	public String getSummerBeginning() {
		return summerBeginning;
	}
	public void setSummerBeginning(String summerBeginning) {
		this.summerBeginning = summerBeginning;
	}
	public String getSummerEnd() {
		return summerEnd;
	}
	public void setSummerEnd(String summerEnd) {
		this.summerEnd = summerEnd;
	}
	public String getSpringBeginning() {
		return springBeginning;
	}
	public void setSpringBeginning(String springBeginning) {
		this.springBeginning = springBeginning;
	}
	public String getSpringEnd() {
		return springEnd;
	}
	public void setSpringEnd(String springEnd) {
		this.springEnd = springEnd;
	}
}
